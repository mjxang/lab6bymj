//Task 3: Discovering Functions

//function that adds two numbers
function addNumbers(num1, num2) {
  return num1 + num2;
}

//call add function and print result
console.log(addNumbers(4, 7));

//multiply function for two numbers
function multiplyNumbers(num1, num2) {
  return num1 * num2;
}

//call multiplyNumbers and print result
console.log(multiplyNumbers(4, 5));
